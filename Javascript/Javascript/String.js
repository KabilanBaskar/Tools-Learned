//String
myVariable = "hello world!"

// length propperty
console.log(myVariable.length)

// character property
console.log(myVariable.charAt(2))

// String position at first
console.log(myVariable.indexOf("i"))

// String Position at last
console.log(myVariable.lastIndexOf("l"))

// Slice or substring
console.log(myVariable.slice(2,4))

// Uppercase and Lowercase
console.log(myVariable.toUpperCase())
console.log(myVariable.toLowerCase())

// Find substring in a String 
console.log(myVariable.includes("wo"))

//convert array using split
console.log(myVariable.split(" "))